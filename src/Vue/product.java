    private void Actualiser() {
        txtid.setText(null);
        txtcategorie.setText(null);
        txtproduit.setText(null);
        txtprix.setText(null);
        txtquantite.setText(null);
    }

    private void TableProduit() {
        List<ModelProduit> listeProduits = mpo.liste();
        model = (DefaultTableModel) jTable1.getModel();

        Object[] ob = new Object[6];
        for (int i = 0; i < listeProduits.size(); i++) {
            ob[0] = listeProduits.get(i).getId();
            ob[1] = listeProduits.get(i).getNomProduit();
            ob[2] = listeProduits.get(i).getCategorie();
            ob[3] = listeProduits.get(i).getQuantite();
            ob[4] = listeProduits.get(i).getPrix();

            model.addRow(ob);
        }
    }

    private void ActualiserTable() {
        for (int i = 0; i < model.getRowCount() ; i++) {
            model.removeRow(i);
            i = i-1;
        }

    }
